<script>
$('div .radio').click(function(){
  var select = $(this).attr('id');
  $(this).siblings('div').children().addClass('hidden');
  $(this).html('<div class="selected" style="width: 8px;height: 8px;border-radius: 4px;background: #0ABBCA;margin:2px;"></div>');
  return $('.'+select).click();
});

function gradeChange(){
  var grade = document.all['typeID'].value
  if (grade == "1"){
      $(".plain1").addClass('hidden');
      $(".plain2").removeClass('hidden');
  }else if(grade == "2"){
      $(".plain1").addClass('hidden');
      $(".plain3").removeClass('hidden');
  }else if(grade == "3"){
      $(".plain1").addClass('hidden');
      $(".plain3").removeClass('hidden');
  }
}


$(".prev").click(function(){
      $(".plain2").addClass('hidden');
      $('.plain3').addClass('hidden');
      $(".plain1").removeClass('hidden');
});

$(".submit").click(function(){


      var id = $(".type").val();
      if(id=="1"){
        var data = $('.form'+id).serializeArray();
      }else{
        var data = $('.form2').serializeArray();
      }
      //alert(data);
      $.ajax({
          type:"POST",
          url:"./action/submit.php",
          data:{id:id,data:data},
          dataType:"Json",
          success:function(data){
            $(".plain2").addClass('hidden');
            $('.plain3').addClass('hidden');
            $(".plain1").addClass('hidden');
            $(".plain4").removeClass('hidden');
          },
          error:function(){
            alert("预约失败");
          }

      });

});
</script>