function gradeChange(){
  var grade = document.all['typeID'].value
  if (grade == "1"){
      $(".plain1").addClass('hidden');
      $(".plain2").removeClass('hidden');
  }else if(grade == "2"){
      $(".plain1").addClass('hidden');
      $(".plain3").removeClass('hidden');
  }else if(grade == "3"){
      $(".plain1").addClass('hidden');
      $(".plain3").removeClass('hidden');
  }
}


$(".prev").click(function(){
      $(".plain2").addClass('hidden');
      $('.plain3').addClass('hidden');
      $(".plain1").removeClass('hidden');
});
</script>